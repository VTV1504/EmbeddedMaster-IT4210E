/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */
  // Initialize SDRAM (used for LCD framebuffer)
  BSP_SDRAM_Init();

  // Initialize the LCD
  BSP_LCD_Init();
  BSP_TS_Init(LCD_WIDTH, LCD_HEIGHT);
  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_CRC_Init();
  MX_DMA2D_Init();
  MX_FMC_Init();
  MX_I2C3_Init();
  MX_LTDC_Init();
  MX_SPI5_Init();
  MX_TIM1_Init();
  MX_USART1_UART_Init();
  MX_USB_OTG_HS_HCD_Init();
  MX_X_CUBE_AI_Init();
  /* USER CODE BEGIN 2 */
  DrawBackgroundUI();
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
	  BSP_TS_GetState(&ts);
	  if (ts.TouchDetected) {
		  uint16_t x = ts.X;
		  uint16_t y = 320-ts.Y;
		  if (in_clear_button(x, y))
		  {
			  clear_canvas();
		  }
		  else if (in_run_button(x, y))
		  {
			  MX_X_CUBE_AI_Process();
			  DisplayResult();
		  }
		  else if (in_canvas_area(x, y))
		  {
			  if (firstTouch){
				  lastX = x;
				  lastY = y;
				  firstTouch = false;
			  }
			  else
			  {
				  BSP_LCD_SetTextColor(LCD_COLOR_BLACK);
				  DrawThickLine(lastX, lastY, x, y);
				  lastX = x;
				  lastY = y;
			  }
		  }
		  touchReleaseTime = HAL_GetTick();
	  }
	  else
	  {
		  if (HAL_GetTick() - touchReleaseTime > 200) {
			  firstTouch = true;
		  }
	  }

    /* USER CODE END WHILE */
    /* USER CODE BEGIN 3 */
	  HAL_Delay(50);
  }
  /* USER CODE END 3 */
}

